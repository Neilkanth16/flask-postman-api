    PULL_DOWN = "PULL_DOWN"
    PAUSE = "PAUSE"
    RESUME = "RESUME"
    STOP = "STOP"
    TICK = "TICK"
    REACHED_TOP = "REACHED_TOP"
    REACHED_BOTTOM = "REACHED_BOTTOM"
    SEAL_CONDITIONS_MET = "SEAL_CONDITIONS_MET"
    SEAL_COMPLETE = "SEAL_COMPLETE"
    REVERSED = "REVERSED"
    ERROR_OCCURRED = "ERROR_OCCURRED"

