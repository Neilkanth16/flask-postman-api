    STOPPED = "STOPPED"
    MOVING_UP = "MOVING_UP"
    MOVING_DOWN = "MOVING_DOWN"
    SLOWING_UP = "SLOWING_UP"
    SLOWING_DOWN = "SLOWING_DOWN"
    STARTING_UP = "STARTING_UP"
    STARTING_DOWN = "STARTING_DOWN"
    SEALING = "SEALING"
    PAUSED = "PAUSED"
    AT_TOP = "AT_TOP"
    AT_BOTTOM = "AT_BOTTOM"
    ERROR = "ERROR"

